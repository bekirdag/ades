def _extend_domain_pack_specs(
    specs: tuple[DomainPackSpec, ...],
) -> tuple[DomainPackSpec, ...]:
    extended_specs: list[DomainPackSpec] = []
    for spec in specs:
        extended_specs.append(
            DomainPackSpec(
                pack_id=spec.pack_id,
                domain=spec.domain,
                description=spec.description,
                entities=(
                    *spec.entities,
                    *BDYA_DOMAIN_PACK_ENTITY_EXTENSIONS.get(spec.pack_id, ()),
                ),
                rules=(
                    *spec.rules,
                    *BDYA_DOMAIN_PACK_RULE_EXTENSIONS.get(spec.pack_id, ()),
                ),
            )
        )
    return tuple(extended_specs)


BDYA_DOMAIN_PACK_SPECS = _extend_domain_pack_specs(BDYA_DOMAIN_PACK_SPECS)


def build_bdya_domain_source_bundles(
    *,
    output_dir: str | Path,
    version: str = "0.2.0",
) -> list[DomainSourceBundleResult]:
    """Build normalized source bundles for curated BDYA domain packs."""

    resolved_output_dir = Path(output_dir).expanduser().resolve()
    resolved_output_dir.mkdir(parents=True, exist_ok=True)
    results: list[DomainSourceBundleResult] = []
    for spec in BDYA_DOMAIN_PACK_SPECS:
        results.append(
            _build_domain_source_bundle(
                spec=spec,
                output_dir=resolved_output_dir,
                version=version,
            )
        )
    return results


def _build_domain_source_bundle(
    *,
    spec: DomainPackSpec,
    output_dir: Path,
    version: str,
) -> DomainSourceBundleResult:
    bundle_dir = output_dir / f"{spec.pack_id}-bundle"
    if bundle_dir.exists():
        shutil.rmtree(bundle_dir)
    normalized_dir = bundle_dir / "normalized"
    source_dir = bundle_dir / "source"
    normalized_dir.mkdir(parents=True, exist_ok=True)
    source_dir.mkdir(parents=True, exist_ok=True)

    source_path = source_dir / "curated_entities.json"
    entities_path = normalized_dir / "entities.jsonl"
    rules_path = normalized_dir / "rules.jsonl"
    bundle_manifest_path = bundle_dir / "bundle.json"

    source_path.write_text(
        json.dumps({"entities": list(spec.entities)}, indent=2, sort_keys=True) + "\n",
        encoding="utf-8",
    )
    _write_jsonl(entities_path, list(spec.entities))
    _write_jsonl(rules_path, list(spec.rules))

    if spec.pack_id == "business-vector-en":
        sources = _build_business_source_entries(spec=spec, source_dir=source_dir)
        if not sources:
            raise ValueError("business-vector-en requires source-family evidence entries.")
        tags = [spec.domain, "bdya", "phase6", "source-backed", "english"]
    elif spec.pack_id == "economics-vector-en":
        sources = _build_economics_source_entries(spec=spec, source_dir=source_dir)
        if not sources:
            raise ValueError("economics-vector-en requires source-family evidence entries.")
        tags = [spec.domain, "bdya", "phase6", "source-backed", "english"]
    elif spec.pack_id == "politics-vector-en":
        sources = _build_politics_source_entries(spec=spec, source_dir=source_dir)
        if not sources:
            raise ValueError("politics-vector-en requires source-family evidence entries.")
        tags = [spec.domain, "bdya", "phase6", "source-backed", "english"]
    else:
        sources = [
            build_bundle_source_entry(
                name=f"curated-{spec.domain}-bdya-phase6",
                snapshot_path=source_path,
                record_count=len(spec.entities),
                adapter="curated_bdya_domain_entities",
            )
        ]
        tags = [spec.domain, "bdya", "phase6", "curated", "english"]
    bundle_manifest_path.write_text(
        json.dumps(
            {
                "schema_version": 1,
                "pack_id": spec.pack_id,
                "version": version,
                "language": "en",
                "domain": spec.domain,
                "tier": "domain",
                "description": spec.description,
                "tags": tags,
                "dependencies": [],
                "entities_path": "normalized/entities.jsonl",
                "rules_path": "normalized/rules.jsonl",
                "alias_resolution": "analyze",
                "label_mappings": {},
                "stoplisted_aliases": ["N/A"],
                "allowed_ambiguous_aliases": [],
                "sources": sources,
            },
            indent=2,
            sort_keys=True,
        )
        + "\n",
        encoding="utf-8",
    )
    write_sources_lock(
        bundle_dir=bundle_dir,
        pack_id=spec.pack_id,
        version=version,
        entities_path="normalized/entities.jsonl",
        rules_path="normalized/rules.jsonl",
        sources=sources,
    )
    return DomainSourceBundleResult(
        pack_id=spec.pack_id,
        version=version,
        bundle_dir=str(bundle_dir),
        bundle_manifest_path=str(bundle_manifest_path),
        entities_path=str(entities_path),
        rules_path=str(rules_path),
        entity_record_count=len(spec.entities),
        rule_record_count=len(spec.rules),
    )


def _write_jsonl(path: Path, records: list[dict[str, Any]]) -> None:
    path.write_text(
        "".join(json.dumps(record, sort_keys=True) + "\n" for record in records),
        encoding="utf-8",
    )


def _utc_timestamp() -> str:
    return datetime.now(tz=UTC).replace(microsecond=0).isoformat().replace("+00:00", "Z")
